class FFNExpert(nn.Module):
    """An FFN expert similar to the FFN in Transformer Encoder Layers."""
    def __init__(self, d_model, hidden_dim):
        super(FFNExpert, self).__init__()
        self.input_projection = nn.Linear(d_model, hidden_dim)
        self.output_projection = nn.Linear(hidden_dim, d_model)
        self.activation = nn.ReLU()

    def forward(self, x):
        x = self.input_projection(x)
        x = self.activation(x)
        x = self.output_projection(x)
        return x


class MoETopK(nn.Module):
    def __init__(self, num_experts, top_k, hidden_dim):
        super(MoETopK, self).__init__()
        self.num_experts = num_experts
        self.top_k = top_k
        self.experts = nn.ModuleList([FFNExpert(hidden_dim, hidden_dim) for _ in range(num_experts)])
        self.gating_network = nn.Linear(hidden_dim, num_experts)

    def forward(self, x):
        # Compute gating weights for each time step
        gates = self.gating_network(x)
        gates = F.softmax(gates, dim=2)  # Softmax over experts dimension

        # Select top-k experts dynamically for each time step
        top_k_values, top_k_indices = torch.topk(gates, self.top_k, dim=2)
        mask = torch.zeros_like(gates).scatter_(2, top_k_indices, 1)
        gates = gates * mask
        gates = F.softmax(gates, dim=2)  # Re-normalize the gates after masking

        # Collect outputs from each expert
        expert_outputs = torch.stack([expert(x) for expert in self.experts], dim=2)  # Shape: (batch, seq_len, num_experts, output_dim)

        # Weighted sum of expert outputs
        output = torch.einsum('bsn,bsno->bso', gates, expert_outputs)  # Using einsum for batched, sequenced weighted sum
        return output


class TransformerEncoderLayerWithMoE(nn.Module):
    def __init__(self, d_model, nhead, hidden_dim, dropout=0.1, num_experts=8,top_k=2):
        super(TransformerEncoderLayerWithMoE, self).__init__()
        self.self_attn = nn.MultiheadAttention(d_model, nhead, dropout=dropout)
        self.moe = MoETopK(num_experts, top_k, d_model)
        # Implementation of Feedforward model
        self.dropout = nn.Dropout(dropout)
        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)

    def forward(self, src, src_mask=None, src_key_padding_mask=None):
        src2 = self.self_attn(src, src, src, attn_mask=src_mask,
                              key_padding_mask=src_key_padding_mask)[0]
        src = src + self.dropout(src2)
        src = self.norm1(src)

        # Utilize mixture of experts
        src2 = self.moe(src)
        src = src + self.dropout(src2)
        src = self.norm2(src)
        return src